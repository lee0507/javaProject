package HW3;

public class P1 {	// 클래스 선언

	public static void main(String[] args) { // 메인으로부터 프로그램시작
		int k30_ii; // k30_ii라는 숫자형 변수를 정의
		k30_ii = 1 + 2; // 변수 k30_ii에 1+2라는 값을 저장 
		System.out.printf("#1-1 : %d\n" , k30_ii);	// 변수 k30_ii를 화면에 출력
		
		k30_ii = 1 + 2 * 3; // 변수 ii에 1+2*3이라는 값을 저장
		System.out.printf("#1-2 : %d\n" , k30_ii); // 변수 k30_ii를 화면에 출력
	}

}

package HW6;

public class Calc6 {//클래스 선언

	public int sum(int k30_a, int k30_b) {//sum이라는 함수 생성하고 파라미터를 숫자형 변수 2개를 준다.
		return k30_a + k30_b;//리턴값으로 a + b를 받는다.
	}
	
	public int sum(int k30_a, int k30_b, int k30_c) {//sum이라는 함수 생성하고 파라미터를 숫자형 변수 3개를 준다.
		return k30_a + k30_b + k30_c;//리턴값으로 a + b + c를 받는다.
	}
	
	public int sum(int k30_a, int k30_b, int k30_c, int k30_d) {//sum이라는 함수 생성하고 파라미터를 숫자형 변수 4개를 준다.
		return k30_a + k30_b + k30_c + k30_d;//리턴값으로 a + b + c + d를 받는다.
	}
	
	public double sum(double k30_a, double k30_b) {//sum이라는 함수 생성하고 파라미터를 실수형 변수 2개를 준다.
		return k30_a + k30_b;//리턴값으로 a + b를 받는다.
	}
}
